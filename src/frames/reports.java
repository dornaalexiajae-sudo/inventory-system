    public void loadReports(){
   DefaultTableModel model = (DefaultTableModel) tblReports.getModel();
    model.setRowCount(0);

    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory_db", "root", "");
        String reportType = reporttype.getSelectedItem().toString();
        String sql = "";

        if (reportType.equals("Low Stocks")) {
            sql = "SELECT * FROM products WHERE quantity <= 5 AND quantity > 0";
        } else if (reportType.equals("Out of Stocks")) { 
            sql = "SELECT * FROM products WHERE quantity = 0";
        } else if (reportType.equals("Available Stocks")) {
            sql = "SELECT * FROM products WHERE quantity > 5";
        } else {
            sql = "SELECT * FROM products";
        }

        PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            int quantity = rs.getInt("quantity");
            double price = rs.getDouble("price");
            double itemValuation = price * quantity;
            
            String descriptionValue;
            if (quantity == 0) {
                descriptionValue = " Needs immediate reorder re-stocking!";
            } else {
                descriptionValue = String.format("Total Value: ₱%.2f", itemValuation);
            }

            model.addRow(new Object[] {
                rs.getInt("product_id"),
                rs.getString("product_name"),
                rs.getString("category"),
                quantity,
                "php" + String.format("%.2f", price), 
                descriptionValue,
                rs.getDate("date") 
            });
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error compilation tracking data: " + e);
    }
    }
