    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        // TODO add your handling code here:
    if (date_from.getDate() == null || date_to.getDate() == null) {
        JOptionPane.showMessageDialog(null, "Please select both 'From' and 'To' dates.");
        return;
    }

    DefaultTableModel model = (DefaultTableModel) tblReports.getModel();
    model.setRowCount(0);

    try {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String dateFrom = sdf.format(date_from.getDate());
        String dateTo = sdf.format(date_to.getDate());

        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory_db", "root", "");
     
        String reportType = reporttype.getSelectedItem().toString();
        String sql = "";

        if (reportType.equals("Low Stocks")) {
            sql = "SELECT * FROM products WHERE (quantity <= 5 AND quantity > 0) AND date BETWEEN ? AND ?";
        } else if (reportType.equals("Out of Stocks")) { 
            sql = "SELECT * FROM products WHERE quantity = 0 AND date BETWEEN ? AND ?";
        } else if (reportType.equals("Available Stocks")) {
            sql = "SELECT * FROM products WHERE quantity > 5 AND date BETWEEN ? AND ?";
        } else {
            sql = "SELECT * FROM products WHERE date BETWEEN ? AND ?";
        }

        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, dateFrom);
        pst.setString(2, dateTo);

        ResultSet rs = pst.executeQuery();

        StringBuilder reportBuilder = new StringBuilder();
        reportBuilder.append("=========================================================================================================\n");
        reportBuilder.append(String.format("                               INVENTORY REPORT (%s)\n", reportType.toUpperCase()));
        reportBuilder.append(String.format("                               Date Range: %s to %s\n", dateFrom, dateTo));
        reportBuilder.append("=========================================================================================================\n\n");
        
    
        reportBuilder.append(String.format("%-25s | %-15s | %-6s | %-12s | %-12s | %-25s\n", 
                "Product Name", "Category", "Qty", "Price", "Date", "Description"));
        reportBuilder.append("---------------------------------------------------------------------------------------------------------\n");

        boolean hasRecords = false;
        while(rs.next()) {
            hasRecords = true;
            int quantity = rs.getInt("quantity");
            double price = rs.getDouble("price");
            String productName = rs.getString("product_name");
            String category = rs.getString("category");
            Date rDate = rs.getDate("date");

            double itemValuation = price * quantity;
            String descriptionValue;
            if (quantity == 0) {
                descriptionValue = "Needs immediate reorder!";
            } else {
                descriptionValue = String.format("Valuation: ₱%.2f", itemValuation);
            }

            model.addRow(new Object[]{
                rs.getInt("product_id"),
                productName,
                category,
                quantity,
                "₱" + String.format("%.2f", price),
                descriptionValue,
                rDate
            });

            reportBuilder.append(String.format("%-25s | %-15s | %-6d | ₱%-11.2f | %-12s | %-25s\n", 
                    productName, 
                    category, 
                    quantity, 
                    price, 
                    (rDate != null ? rDate.toString() : "N/A"), 
                    descriptionValue));
        }
        
        reportBuilder.append("==================================================================================================\n");

        if (!hasRecords) {
            JOptionPane.showMessageDialog(null, "No data matches your specified date range filters.", "Report Summary", JOptionPane.INFORMATION_MESSAGE);
        } else { 
            javax.swing.JTextArea textArea = new javax.swing.JTextArea(reportBuilder.toString());
            textArea.setFont(new java.awt.Font("Monospaced", java.awt.Font.PLAIN, 12));
            textArea.setEditable(false);

            javax.swing.JScrollPane scrollPane = new javax.swing.JScrollPane(textArea);
            scrollPane.setPreferredSize(new java.awt.Dimension(850, 450));

            JOptionPane.showMessageDialog(null, scrollPane, "Generated Text Report Summary", JOptionPane.PLAIN_MESSAGE);
        }
    } catch(Exception e) {
        JOptionPane.showMessageDialog(null, "Error tracking date reports: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_jButton17ActionPerformed

