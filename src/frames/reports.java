    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed

    if (date_from.getDate() == null || date_to.getDate() == null) {
        JOptionPane.showMessageDialog(null, "Please select both 'From' and 'To' dates before exporting.");
        return;
    }

    javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
    fileChooser.setDialogTitle("Specify a File Location to Save Your PDF Report");
   
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    String dateFrom = sdf.format(date_from.getDate());
    String dateTo = sdf.format(date_to.getDate());
    
    fileChooser.setSelectedFile(new java.io.File("Inventory_Report_" + dateFrom + "_to_" + dateTo + ".pdf"));

    int userSelection = fileChooser.showSaveDialog(this);
    if (userSelection != javax.swing.JFileChooser.APPROVE_OPTION) {
        return; 
    }

    java.io.File fileToSave = fileChooser.getSelectedFile();
    String pdfPath = fileToSave.getAbsolutePath();
    
    if (!pdfPath.toLowerCase().endsWith(".pdf")) {
        pdfPath += ".pdf";
    }

    try {
        com.lowagie.text.Document document = new com.lowagie.text.Document(com.lowagie.text.PageSize.A4, 36, 36, 54, 36);
        com.lowagie.text.pdf.PdfWriter.getInstance(document, new java.io.FileOutputStream(pdfPath));
        
        document.open();

        com.lowagie.text.Font titleFont = new com.lowagie.text.Font(com.lowagie.text.Font.HELVETICA, 18, com.lowagie.text.Font.BOLD, java.awt.Color.WHITE);
        com.lowagie.text.Font metaFont = new com.lowagie.text.Font(com.lowagie.text.Font.HELVETICA, 10, com.lowagie.text.Font.NORMAL, java.awt.Color.DARK_GRAY);
        com.lowagie.text.Font headFont = new com.lowagie.text.Font(com.lowagie.text.Font.HELVETICA, 10, com.lowagie.text.Font.BOLD, java.awt.Color.WHITE);
        com.lowagie.text.Font cellFont = new com.lowagie.text.Font(com.lowagie.text.Font.HELVETICA, 9, com.lowagie.text.Font.NORMAL, java.awt.Color.BLACK);

        com.lowagie.text.pdf.PdfPTable headerBanner = new com.lowagie.text.pdf.PdfPTable(1);
        headerBanner.setWidthPercentage(100);
        com.lowagie.text.pdf.PdfPCell bannerCell = new com.lowagie.text.pdf.PdfPCell(new com.lowagie.text.Paragraph("INVENTORY SYSTEM SUMMARY REPORT", titleFont));
        bannerCell.setBackgroundColor(new java.awt.Color(0, 78, 181)); 
        bannerCell.setPadding(12);
        bannerCell.setHorizontalAlignment(com.lowagie.text.Element.ALIGN_CENTER);
        bannerCell.setBorder(com.lowagie.text.Rectangle.NO_BORDER);
        headerBanner.addCell(bannerCell);
        document.add(headerBanner);

        String reportType = reporttype.getSelectedItem().toString();
        document.add(new com.lowagie.text.Paragraph("\nReport Parameter Filter Type: " + reportType, metaFont));
        document.add(new com.lowagie.text.Paragraph("Date Filtering Bounds: " + dateFrom + " through " + dateTo, metaFont));
        document.add(new com.lowagie.text.Paragraph("Generated System Date: " + date.getText() + " at " + Time.getText() + "\n\n", metaFont));

        com.lowagie.text.pdf.PdfPTable pdfTable = new com.lowagie.text.pdf.PdfPTable(7);
        pdfTable.setWidthPercentage(100);
        pdfTable.setWidths(new float[]{1.0f, 2.5f, 2.0f, 1.2f, 1.8f, 3.0f, 1.8f}); 

        String[] headers = {"Prod ID", "Product Name", "Category", "Quantity", "Price", "Description Valuation", "Log Date"};
        for (String headerText : headers) {
            com.lowagie.text.pdf.PdfPCell hCell = new com.lowagie.text.pdf.PdfPCell(new com.lowagie.text.Paragraph(headerText, headFont));
            hCell.setBackgroundColor(new java.awt.Color(15, 23, 42)); 
            hCell.setPadding(8);
            hCell.setHorizontalAlignment(com.lowagie.text.Element.ALIGN_CENTER);
            pdfTable.addCell(hCell);
        }

        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory_db", "root", "");
        String sql = "";

        if (reportType.equals("Low Stocks")) {
            sql = "SELECT * FROM products WHERE (quantity <= 5 AND quantity > 0) AND date BETWEEN ? AND ?";
        } else if (reportType.equals("Out of Stocks")) { 
            sql = "SELECT * FROM products WHERE quantity = 0 AND date BETWEEN ? AND ?";
        } else if (reportType.equals("Available Stocks")) {
            sql = "SELECT * FROM products WHERE quantity > 5 AND date BETWEEN ? AND ?";
        } else {
            sql = "SELECT * FROM products WHERE date BETWEEN ? AND ?";
        }

        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, dateFrom);
        pst.setString(2, dateTo);
        ResultSet rs = pst.executeQuery();

        boolean rowAlternateColor = false;
        double overallGrandTotalAssetPool = 0;
        int documentedItemCount = 0;

        while (rs.next()) {
            documentedItemCount++;
            int id = rs.getInt("product_id");
            String name = rs.getString("product_name");
            String cat = rs.getString("category");
            int qty = rs.getInt("quantity");
            double price = rs.getDouble("price");
            java.sql.Date dLog = rs.getDate("date");

            double valuation = price * qty;
            overallGrandTotalAssetPool += valuation;

            String descStr = (qty == 0) ? "Needs Immediate Restock!" : String.format("Total: PHP %.2f", valuation);
            String dateStr = (dLog != null) ? dLog.toString() : "N/A";

            String[][] rowCells = {
                {String.valueOf(id), "C"}, {name, "L"}, {cat, "L"}, 
                {String.valueOf(qty), "C"}, {String.format("PHP %.2f", price), "R"}, 
                {descStr, "L"}, {dateStr, "C"}
            };

            for (String[] cellData : rowCells) {
                com.lowagie.text.pdf.PdfPCell dataCell = new com.lowagie.text.pdf.PdfPCell(new com.lowagie.text.Paragraph(cellData[0], cellFont));
                dataCell.setPadding(6);
                
                if (cellData[1].equals("C")) dataCell.setHorizontalAlignment(com.lowagie.text.Element.ALIGN_CENTER);
                else if (cellData[1].equals("R")) dataCell.setHorizontalAlignment(com.lowagie.text.Element.ALIGN_RIGHT);
                else dataCell.setHorizontalAlignment(com.lowagie.text.Element.ALIGN_LEFT);
                if (rowAlternateColor) {
                    dataCell.setBackgroundColor(new java.awt.Color(245, 247, 250));
                } else {
                    dataCell.setBackgroundColor(java.awt.Color.WHITE);
                }
                pdfTable.addCell(dataCell);
            }
            rowAlternateColor = !rowAlternateColor; 
        }

        document.add(pdfTable);

        document.add(new com.lowagie.text.Paragraph("\n------------------------------------------------------------------------------------------------------------------------"));
        com.lowagie.text.Font summaryFont = new com.lowagie.text.Font(com.lowagie.text.Font.HELVETICA, 11, com.lowagie.text.Font.BOLD, new java.awt.Color(30, 41, 59));
        document.add(new com.lowagie.text.Paragraph(String.format("Total Unique Items Documented: %d rows", documentedItemCount), summaryFont));
        document.add(new com.lowagie.text.Paragraph(String.format("Total Cumulative Inventory Valuation: PHP %.2f", overallGrandTotalAssetPool), summaryFont));
        document.add(new com.lowagie.text.Paragraph("------------------------------------------------------------------------------------------------------------------------"));

        document.close();
        con.close();

        if (java.awt.Desktop.isDesktopSupported()) {
            java.awt.Desktop.getDesktop().open(new java.io.File(pdfPath));
        } else {
            JOptionPane.showMessageDialog(null, "PDF successfully saved to:\n" + pdfPath, "Export Successful", JOptionPane.INFORMATION_MESSAGE);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error writing PDF file: " + e.getMessage(), "PDF Export Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton13ActionPerformed

