    private void blogin1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_blogin1ActionPerformed
        // TODO add your handling code here:
       try {
        int selectedRow = tblUsers.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a user first.");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this user?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory_db", "root", "");
            
            String sql = "DELETE FROM proflie_users WHERE user_id = ?"; 
            
            PreparedStatement pst = con.prepareStatement(sql);

            int userID = Integer.parseInt(tblUsers.getValueAt(selectedRow, 0).toString());
            
            pst.setInt(1, userID);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "User Deleted Successfully");
            refreshTable();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error deleting user: " + e.getMessage());
    }

    }//GEN-LAST:event_blogin1ActionPerformed

