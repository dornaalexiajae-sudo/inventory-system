   public void refreshTable() {
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblUsers.getModel();
    model.setRowCount(0); 

    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory_db", "root", "");
        String sql = "SELECT * FROM proflie_users";
        PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("user_id"),
                rs.getString("first_name"), 
                rs.getString("last_name"),  
                rs.getString("address"),   
                rs.getString("phone_number"),
                rs.getInt("age"),          
                rs.getString("civil_status"),
                rs.getString("role"),      
                rs.getString("username"),   
                rs.getString("password")    
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error loading table: " + e.getMessage());
    }

}
