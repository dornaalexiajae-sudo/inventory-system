    private void buttonOpenSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOpenSignupActionPerformed
        // TODO add your handling code here:
       try {
    Connection con =
    DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory_db","root","");

    String civilStatus = "";
    if(rSingle.isSelected()) {
        civilStatus = "Single";
    } else if(rTaken.isSelected()) {
        civilStatus = "Taken";
    }else if(rMarried.isSelected()) {
        civilStatus = "Married";
    }

    String sql ="INSERT INTO proflie_users " +
    "(first_name, last_name, address, " +
    "phone_number, age, civil_status, " +
    "role, username, password) " +
    "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";

    PreparedStatement pst = con.prepareStatement(sql);
    pst.setString(1,firstname.getText());
    pst.setString(2,lastname.getText());
    pst.setString(3,address.getText());
    pst.setString(4,phone_num.getText());
    pst.setInt(5,Integer.parseInt(age.getText()));
    pst.setString(6,civilStatus);
    pst.setString(7,role_id.getSelectedItem().toString());
    pst.setString(8,username.getText());
    pst.setString(9,password.getText());
    pst.executeUpdate();
    JOptionPane.showMessageDialog(null, "Signup Successful");
    refreshTable();
    
    JOptionPane.showMessageDialog(null,"Signup Successful");

} catch(Exception e) {
    JOptionPane.showMessageDialog(null,e);

}
      
    }//GEN-LAST:event_buttonOpenSignupActionPerformed

