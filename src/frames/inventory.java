    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        // TODO add your handling code here:
     String selectedProduct = soproduct.getSelectedItem().toString();
     String qtyText = soquantity.getText().trim();
    
    if(qtyText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a quantity.");
        return;
    }
    
    try {
        int qtyToRemove = Integer.parseInt(qtyText);
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory_db", "root", "");
        
        String checkSql = "SELECT quantity FROM products WHERE product_name = ?";
        PreparedStatement checkPst = con.prepareStatement(checkSql);
        checkPst.setString(1, selectedProduct);
        ResultSet rs = checkPst.executeQuery();
        
        if(rs.next()) {
            int currentQty = rs.getInt("quantity");
            if(qtyToRemove > currentQty) {
                JOptionPane.showMessageDialog(this, "Not enough stock! Current stock: " + currentQty);
                con.close();
                return;
            }
        }
        
        String sql = "UPDATE products SET quantity = quantity - ? WHERE product_name = ?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setInt(1, qtyToRemove);
        pst.setString(2, selectedProduct);
        
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Stock removed successfully!");
        soquantity.setText("");
        loadInventory();
        con.close();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter a valid number.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }

    }//GEN-LAST:event_jButton14ActionPerformed

