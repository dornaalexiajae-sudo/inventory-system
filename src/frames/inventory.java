    public void loadproducts(){    
    
      try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory_db","root","");
        String sql = "SELECT product_name FROM products";
        PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        // Clear all three dropdowns first
        siproduct.removeAllItems();
        soproduct.removeAllItems();
        upproduct.removeAllItems();

        while(rs.next()) {
            String name = rs.getString("product_name");
            siproduct.addItem(name);
            soproduct.addItem(name);
            upproduct.addItem(name);
        }
        con.close(); 
    } catch(Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}
    
    public void loadInventory() {
        DefaultTableModel model = (DefaultTableModel) tblInventory.getModel();
    model.setRowCount(0);

    try {
        Connection con = DriverManager.getConnection( "jdbc:mysql://localhost:3306/inventory_db","root","");
        String sql = "SELECT * FROM products";

        PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while(rs.next()) {
        model.addRow(new Object[] {rs.getInt("product_id"),rs.getString("product_name"), rs.getString("category"), rs.getInt("quantity"),rs.getDouble("price"),rs.getString("pddescription")});

        }
    } catch(Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }
